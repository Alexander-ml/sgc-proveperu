package com.proveperu.m04_caja_pagos.enums;
/**
 * Estados operativos de una caja.
 */
public enum EstadoCaja {
    /**
     * Caja disponible para registrar movimientos.
     */
    ABIERTA,

    /**
     * Caja cerrada temporalmente.
     */
    CERRADA,

    /**
     * Caja fuera de servicio.
     */
    INACTIVA
}
